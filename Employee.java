package Pojo;

public class Employee {
	
	private int empid;
	public  String name;
	protected double salary;
	
public void setid(int empid)
{
	this.empid=empid;
}
public int getid()
{
	return empid;
	
}

public void setname(String name) {
	
	this.name=name;
}
public String getname()
{
	return name;
	
}

public void setsalary(double salary)
{
	this.salary=salary;
	
}

public double getsalary()
{
	return salary;
	
}
		



}
